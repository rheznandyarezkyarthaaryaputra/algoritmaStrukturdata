public class HashTableMain1 {

    public static void main(String[] args) {
        // Printing number 0 twice
        System.out.println(0);
        System.out.println(0);

        // Loop from 1 to 3 and print each number
        for (int i = 1; i <= 3; i++) {
            System.out.println(i);
        }
    }
}
